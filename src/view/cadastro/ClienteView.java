    public void setCliente(Cliente cli) {
        Date data = cli.getDataCadastro();
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = dateFormat.format(data);

        obj = new Cliente();
        habilitarFormulario(true);
        jtfCodigo.setValue(cli.getId());
        jtfNome.setText(cli.getNome());
        jtfDataCadastro.setText(dataFormatada);
        jtfTelefone.setText(cli.getTelefone());
        jtfRua.setText(cli.getRua());
        jtfNumero.setText(cli.getNumero());
        jtfBairro.setText(cli.getBairro());
        jtfCidade.setText(cli.getCidade());
        combo();
        jcbEstado.setSelectedItem(cli.getEstado().getNome());

        if (cli.getTipo().equals("F")) {
            rbtFisica.setSelected(true);
            jtfCpf.setEnabled(true);
        } else if (cli.getTipo().equals("J")) {
            rbtJuridica.setSelected(true);
            jtfCnpj.setEnabled(true);
            jtfRazaoSocial.setEnabled(true);
        }
        //jcbUnidade.setSelectedItem(produto.getUnidade().getSigla());
        pr = "Alterado";
    }

    private void combo() {
        estadoDAO = new EstadoDAO();

        estados = estadoDAO.buscarEstado();
        jcbEstado.removeAllItems();
        for (Estado ma : estados) {
            jcbEstado.addItem(ma.getNome());
        }
        jcbEstado.setSelectedIndex(-1);

    }

