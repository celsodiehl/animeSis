    private boolean validaCampos() {
        if (!(jtfNome.getText().length() > 0 && jtfNome.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo NOME Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jtfNome.requestFocus();
            return false;
        }
        if (!(jtfCidade.getText().length() > 0 && jtfCidade.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo CIDADE Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jtfCidade.requestFocus();
            return false;
        }
        if (!(jcbEstado.getSelectedItem() != null)) {
            JOptionPane.showMessageDialog(null, "Campo ESTADO Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jcbEstado.requestFocus();
            return false;
        }
        if (!(rbtFisica.isSelected() || rbtJuridica.isSelected())) {
            JOptionPane.showMessageDialog(null, "Campo TIPO Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            rbtFisica.requestFocus();
            return false;
        }
        return true;
    }

