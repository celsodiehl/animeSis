    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            if (!jtfCodigo.getText().equals("")) {
                obj.setId(Long.parseLong(jtfCodigo.getText()));
            }
            obj.setEmpresa(LoginView.emp);
            if (rbtFisica.isSelected()) {
                obj.setTipo("F");
            } else if (rbtJuridica.isSelected()) {
                obj.setTipo("J");
            }
            obj.setNome(ftfFornecedor.getText());
            obj.setTelefone(jtfTelefone.getText());
            obj.setEndereco(jtfEndereco.getText());
            obj.setBairro(jtfBairro.getText());
            obj.setCidade(jtfCidade.getText());
            obj.setEstado(estados.get(jcbEstado.getSelectedIndex()));
            obj.setEmail(jtfEmail.getText());
            obj.setSite(jtfSite.getText());
            obj.setContato(jtfContato.getText());
            obj.setContatotelefone(jtfContato_tel.getText());

            try {
                dao.salvar(obj);
                JOptionPane.showMessageDialog(this, "Fornecedor " + pr + " Com Sucesso.! ");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao inserir Fornecedor.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                //System.out.println(" Cliente:" + venda.getCliente().getCodigo() + " - data:" + venda.getDataVenda() + " - Venda: " + venda.getId());
                return;
            }
            //  } else {
            //                try {
            //                    dao.alterar(cli);
            //                } catch (Exception ex) {
            //                    JOptionPane.showMessageDialog(this, "Erro ao alterar a Nota.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            //                    return;
            //                }
            //  }
            habilitarFormulario(false);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

