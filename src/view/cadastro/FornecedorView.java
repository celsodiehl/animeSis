    public void setFornecedor(Fornecedor e) {
        obj = new Fornecedor();
        habilitarFormulario(true);
        jtfCodigo.setValue(e.getId());
        ftfFornecedor.setValue(e);
        if (e.getTipo().equals("F")) {
            rbtFisica.setSelected(true);
        } else if (e.getTipo().equals("J")) {
            rbtJuridica.setSelected(true);
        }
        jtfTelefone.setText(e.getTelefone());
        jtfEndereco.setText(e.getEndereco());
        jtfBairro.setText(e.getBairro());
        jtfCidade.setText(e.getCidade());
        combo();
        jcbEstado.setSelectedItem(e.getEstado().getNome());
        jtfEmail.setText(e.getEmail());
        jtfSite.setText(e.getSite());
        jtfContato.setText(e.getContato());
        jtfContato_tel.setText(e.getContatotelefone());
        pr = "Alterado";
    }

    private void combo() {
        estadoDAO = new EstadoDAO();

        estados = estadoDAO.buscarEstado();
        jcbEstado.removeAllItems();
        for (Estado ma : estados) {
            jcbEstado.addItem(ma.getNome());
        }
        jcbEstado.setSelectedIndex(-1);
    }

    private void habilitarFormulario(boolean ativo) {
        ftfFornecedor.setEnabled(ativo);
        jtfTelefone.setEnabled(ativo);
        jtfEndereco.setEnabled(ativo);
        jtfBairro.setEnabled(ativo);
        jtfCidade.setEnabled(ativo);
        jcbEstado.setEnabled(ativo);
        jtfEmail.setEnabled(ativo);
        jtfSite.setEnabled(ativo);
        jtfContato.setEnabled(ativo);
        jtfContato_tel.setEnabled(ativo);
        rbtFisica.setEnabled(ativo);
        rbtJuridica.setEnabled(ativo);
        bgTipo.clearSelection();
        btnNovo.setEnabled(!ativo);
        btnSalvar.setEnabled(ativo);
        jtfCpf.setEnabled(ativo);
        jtfCnpj.setEnabled(ativo);
        //btRemoverItem.setEnabled(ativo);
        //tbGradeItens.setEnabled(!ativo);
        if (!ativo) {
            limpaFormulario();
        }
    }

    private void limpaFormulario() {
        obj = null;
        jtfCodigo.setText(null);
        ftfFornecedor.setText(null);
        jtfTelefone.setText(null);
        jtfEndereco.setText(null);
        jtfBairro.setText(null);
        jtfCidade.setText(null);
        jcbEstado.setSelectedItem(null);
        jtfEmail.setText(null);
        jtfSite.setText(null);
        jtfContato.setText(null);
        jtfContato_tel.setText(null);
    }

    private boolean validaCampos() {
        if (!(ftfFornecedor.getText().length() > 0 && ftfFornecedor.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo NOME Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            ftfFornecedor.requestFocus();
            return false;
        }
        if (!(rbtFisica.isSelected() || rbtJuridica.isSelected())) {
            JOptionPane.showMessageDialog(null, "Campo TIPO Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            rbtFisica.requestFocus();
            return false;
        }
        if (!(jtfCidade.getText().length() > 0 && jtfCidade.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo CIDADE Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jtfCidade.requestFocus();
            return false;
        }
        if (!(jcbEstado.getSelectedItem() != null)) {
            JOptionPane.showMessageDialog(null, "Campo ESTADO Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jcbEstado.requestFocus();
            return false;
        }
        return true;
    }

