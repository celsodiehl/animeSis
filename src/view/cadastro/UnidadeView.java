    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            if (!jtfCodigo.getText().equals("")) {
                obj.setId(Long.parseLong(jtfCodigo.getText()));
            }
            obj.setEmpresa(emp);
            obj.setNome(jtfNome.getText());
            obj.setSigla(jtfSigla.getText());
            try {
                dao.salvar(obj);
                JOptionPane.showMessageDialog(this, "Unidade de Medida Cadastrada ! ");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro ao inserir a Unidade.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
            habilitarFormulario(false);
            carregarGrade();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

