    private void habilitarFormulario(boolean ativo) {
        btnNovo.setEnabled(!ativo);
        btnSalvar.setEnabled(ativo);
        jtfNome.setEnabled(ativo);
        jtfSigla.setEnabled(ativo);
        //btExcluir.setEnabled(ativo);
        //btCancelar.setEnabled(ativo);
        if (!ativo) {
            limpaFormulario();
        }
    }

    private void limpaFormulario() {
        obj = null;
        jtfCodigo.setText(null);
        jtfNome.setText(null);
        jtfSigla.setText(null);
    }

    private boolean validaCampos() {
        if (!(jtfSigla.getText().length() > 0 && jtfSigla.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo SIGLA Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jtfSigla.requestFocus();
            return false;
        }
        if (!(jtfNome.getText().length() > 0 && jtfNome.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo DESCRIÇÃO Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            jtfNome.requestFocus();
            return false;
        }
        return true;
    }

    private void carregarGrade() {

        //Generico gDAO = new Generico();
        UnidadeTableModel tm = (UnidadeTableModel) tbGrade.getModel();
        try {
            tm.setDados(dao.listarTodos(emp.getId()));

            DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
            DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
            DefaultTableCellRenderer direita = new DefaultTableCellRenderer();

            esquerda.setHorizontalAlignment(SwingConstants.LEFT);
            centralizado.setHorizontalAlignment(SwingConstants.CENTER);
            direita.setHorizontalAlignment(SwingConstants.RIGHT);

            tbGrade.getColumnModel().getColumn(0).setCellRenderer(direita);
            tbGrade.getColumnModel().getColumn(1).setCellRenderer(esquerda);
            tbGrade.getColumnModel().getColumn(2).setCellRenderer(centralizado);

            tbGrade.getColumnModel().getColumn(0).setPreferredWidth(60);
            tbGrade.getColumnModel().getColumn(1).setPreferredWidth(250);
            tbGrade.getColumnModel().getColumn(2).setPreferredWidth(30);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar grade.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

