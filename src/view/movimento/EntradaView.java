    private void tbGradeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbGradeMouseClicked
        if (evt.getClickCount() == 2) {
            abasVenda.setSelectedIndex(0);
            int linhaSelecionada = tbGrade.getSelectedRow();
            EntradaTableModel vtm = (EntradaTableModel) tbGrade.getModel();
            obj = vtm.getRowValue(linhaSelecionada);

            jtfCodFornecedor.setValue(obj.getFornecedor().getId());
            ftfFornecedor.setValue(obj.getFornecedor());
            jtfDataEntrada.setValue(obj.getDataentrada());
            jtfDataEmissao.setValue(obj.getDataemissao());
            ftfSubTotal.setValue(obj.getVlrTotal());

            EntradaItemTableModel ivtm = (EntradaItemTableModel) tbGradeItens.getModel();
            ivtm.setDados(obj.getItens());

            DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
            DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
            DefaultTableCellRenderer direita = new DefaultTableCellRenderer();

            esquerda.setHorizontalAlignment(SwingConstants.LEFT);
            centralizado.setHorizontalAlignment(SwingConstants.CENTER);
            direita.setHorizontalAlignment(SwingConstants.RIGHT);

            tbGradeItens.getColumnModel().getColumn(0).setCellRenderer(direita);
            tbGradeItens.getColumnModel().getColumn(1).setCellRenderer(esquerda);
            tbGradeItens.getColumnModel().getColumn(2).setCellRenderer(centralizado);
            tbGradeItens.getColumnModel().getColumn(3).setCellRenderer(direita);
            tbGradeItens.getColumnModel().getColumn(4).setCellRenderer(direita);

            tbGradeItens.getColumnModel().getColumn(0).setPreferredWidth(15);
            tbGradeItens.getColumnModel().getColumn(1).setPreferredWidth(290);
            tbGradeItens.getColumnModel().getColumn(2).setPreferredWidth(30);
            tbGradeItens.getColumnModel().getColumn(3).setPreferredWidth(35);
            tbGradeItens.getColumnModel().getColumn(4).setPreferredWidth(35);
            habilitarFormulario(true);
        }
    }//GEN-LAST:event_tbGradeMouseClicked

