    private boolean validarFormulario() {
        if (ftfFornecedor.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Fornecedor Inválido!", "Alerta", JOptionPane.WARNING_MESSAGE);
            ftfFornecedor.requestFocus();
            return false;
        }
        if (!(jtfNumero.getText().length() > 0 && jtfNumero.getText() != null)) {
            JOptionPane.showMessageDialog(this, "Número da NF não pode ser em Branco.!", "Alerta", JOptionPane.WARNING_MESSAGE);
            jtfNumero.requestFocus();
            return false;
        }
        if (!(jtfSerie.getText().length() > 0 && jtfSerie.getText() != null)) {
            JOptionPane.showMessageDialog(this, "Série da NF não pode ser em Branco.!", "Alerta", JOptionPane.WARNING_MESSAGE);
            jtfSerie.requestFocus();
            return false;
        }
//        if (obj.quantidadeItens() == 0) {
//            JOptionPane.showMessageDialog(this, "Quantidade de itens inválida.", "Alerta", JOptionPane.WARNING_MESSAGE);
//            //tpConteudo.setSelectedIndex(1);
//            return false;
//        }
        return true;
    }

    private boolean validarFormularioItens() {
        if (ftfProdutoEntrada.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Produto inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            ftfProdutoEntrada.requestFocus();
            return false;
        }
        return true;
    }

