    private void cartao(Venda venda) {
        ConciliacaoDAO carDAO = new ConciliacaoDAO();
        Calendar calendar = Calendar.getInstance();
        if (venda.getForma().getQtdParcela() > 0) {
            for (int i = 1; i <= venda.getForma().getQtdParcela(); i++) {
                Conciliacao cartao = new Conciliacao();

                cartao.setVenda(venda);
                cartao.setEmpresa(emp);
                cartao.setCliente(venda.getCliente());
                cartao.setPgcondicao(null);
                cartao.setDataPagamento(null);
                cartao.setParcela(i);
                cartao.setJuro(0.0);
                //vendaRecibo.setDesconto(0);
                cartao.setVlrTotal(venda.getVlrsubtotal() / venda.getForma().getQtdParcela());
                cartao.setMulta(0.0);
                calendar.add(Calendar.MONTH, 1);
                cartao.setDataVencimento(calendar.getTime());

                try {
                    carDAO.salvar(cartao);
                } catch (Exception ex) {
                    Logger.getLogger(VendaView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

