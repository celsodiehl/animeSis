    private boolean validarFinalizar() {
        if (jcbForma.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma FORMA DE PAGAMENTO.!", "Alerta", JOptionPane.WARNING_MESSAGE);
            jcbForma.requestFocus();
            return false;
        }
        return true;
    }

    private boolean validarFormulario() {
        if (ftfCliente.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Cliente inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            ftfCliente.requestFocus();
            return false;
        }

        if (venda.quantidadeItens() == 0) {
            JOptionPane.showMessageDialog(this, "Quantidade de Itens Inválida.", "Alerta", JOptionPane.WARNING_MESSAGE);
            //tpConteudo.setSelectedIndex(1);
            return false;
        }
        return true;
    }

    private boolean validarFormularioItens() {
        if (!(ftfQuantidade.getText().length() > 0 && ftfQuantidade.getText() != null)) {
            JOptionPane.showMessageDialog(null, "Campo QUANTIDADE Deve ser Informado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            ftfQuantidade.requestFocus();
            return false;
        }
        if (ftfProduto.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Produto Inválido.", "Alerta", JOptionPane.WARNING_MESSAGE);
            ftfProduto.requestFocus();
            return false;
        }
        return true;
    }

